var color = "red";

if (color == 'blue') {
    console.log('The coloe is: blue');
}
else if (color == 'green') {
    console.log('The coloe is: green');
}
else if (color == 'yellow') {
    console.log('The coloe is: yellow');
}
else if (color == 'red') {
    console.log('The coloe is: red');
}
else if (color == 'white') {
    console.log('The coloe is: white');
}
else {
    console.log('The coloe is: black');
}

//Javascript switch case break and default
switch (color) {
    case 'blue':
        console.log('The color is: blue');
        break;
    case 'green':
        console.log('The color is: green');
        break;
    case 'yellow':
        console.log('The color is: Yellow');
        break;
    case 'red':
        console.log('The color is: red');
        break;
    case 'white':
        console.log('The color is: white');
        break;
    default:
        console.log('The coloe is: black');
}
